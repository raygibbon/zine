static int parse_key_value(const char *line, char *key, int key_size, char *value, int value_size) {
    const char *colon = strchr(line, ':');

    if (colon == NULL) {
        return 0;
    }

    if (colon == line) {
        return 0;
    }

    copy_range_string(key, key_size, line, colon);
    trim_whitespace(key);

    ++colon;
    while (*colon == ' ' || *colon == '\t') {
        ++colon;
    }

    copy_string(value, value_size, colon);
    trim_whitespace(value);
    return 1;
}

int load_site_config(const char *path, SiteConfig *config) {
    FILE *fp;
    int in_about;

    init_site_config(config);
    g_content_error[0] = '\0';
    fp = open_existing_file(path, "r");
    if (fp == NULL) {
        set_content_error("Failed to open site.txt");
        return 0;
    }

    in_about = 0;
    config->about[0] = '\0';

    while (fgets(g_parse_line, sizeof(g_parse_line), fp) != NULL) {
        trim_line_endings(g_parse_line);

        if (in_about) {
            if (!parse_key_value(g_parse_line, g_parse_key, sizeof(g_parse_key), g_parse_value, sizeof(g_parse_value)) || !is_site_config_key(g_parse_key)) {
                if (!append_field_line(config->about, sizeof(config->about), g_parse_line, "about", path)) {
                    fclose(fp);
                    return 0;
                }
                continue;
            }

            in_about = 0;
        }

        if (g_parse_line[0] == '\0') {
            continue;
        }

        if (!parse_key_value(g_parse_line, g_parse_key, sizeof(g_parse_key), g_parse_value, sizeof(g_parse_value))) {
            continue;
        }

        if (strings_equal(g_parse_key, "title")) {
            if (!copy_field_value(config->title, sizeof(config->title), g_parse_value, "title", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "banner")) {
            if (!copy_field_value(config->banner, sizeof(config->banner), g_parse_value, "banner", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "theme")) {
            if (!copy_field_value(config->theme, sizeof(config->theme), g_parse_value, "theme", path)) {
                fclose(fp);
                return 0;
            }
            lowercase_string(config->theme);
            if (!strings_equal(config->theme, "vga") &&
                    !strings_equal(config->theme, "win98") &&
                    !strings_equal(config->theme, "crt")) {
                sprintf(g_content_message, "Invalid theme '%s' in %s", config->theme, path);
                set_content_error(g_content_message);
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "tagline")) {
            if (!copy_field_value(config->tagline, sizeof(config->tagline), g_parse_value, "tagline", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "description")) {
            if (!copy_field_value(config->description, sizeof(config->description), g_parse_value, "description", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "about")) {
            if (!copy_field_value(config->about, sizeof(config->about), g_parse_value, "about", path)) {
                fclose(fp);
                return 0;
            }
            in_about = 1;
        }
    }

    fclose(fp);
    return 1;
}

static int load_post_file(const char *path, const char *source_name, Post *post) {
    FILE *fp;
    int in_body;

    init_post(post);
    copy_string(post->source_name, sizeof(post->source_name), source_name);
    copy_string(post->source_path, sizeof(post->source_path), path);

    fp = open_existing_file(path, "r");
    if (fp == NULL) {
        sprintf(g_content_message, "Failed to open %s", path);
        set_content_error(g_content_message);
        return 0;
    }

    in_body = 0;

    while (fgets(g_parse_line, sizeof(g_parse_line), fp) != NULL) {
        trim_line_endings(g_parse_line);

        if (in_body) {
            if (g_parse_line[0] != '\0') {
                post->has_body = 1;
            }
            continue;
        }

        if (g_parse_line[0] == '\0') {
            post->body_offset = ftell(fp);
            in_body = 1;
            continue;
        }

        if (!parse_key_value(g_parse_line, g_parse_key, sizeof(g_parse_key), g_parse_value, sizeof(g_parse_value))) {
            continue;
        }

        if (strings_equal(g_parse_key, "title")) {
            if (!copy_field_value(post->title, sizeof(post->title), g_parse_value, "title", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "slug")) {
            if (!copy_field_value(post->slug, sizeof(post->slug), g_parse_value, "slug", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "date")) {
            if (!copy_field_value(post->date, sizeof(post->date), g_parse_value, "date", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "category")) {
            if (!copy_field_value(post->category, sizeof(post->category), g_parse_value, "category", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "summary")) {
            if (!copy_field_value(post->summary, sizeof(post->summary), g_parse_value, "summary", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "tags")) {
            if (!copy_field_value(post->tags, sizeof(post->tags), g_parse_value, "tags", path)) {
                fclose(fp);
                return 0;
            }
        } else if (strings_equal(g_parse_key, "draft")) {
            post->draft = strings_equal_ignore_case(g_parse_value, "yes") || strings_equal_ignore_case(g_parse_value, "true") || strings_equal(g_parse_value, "1");
        }
    }

    normalize_slug(post->slug, sizeof(post->slug), post->slug);
    normalize_slug(post->category, sizeof(post->category), post->category);
    make_output_name(post->output_name, sizeof(post->output_name), post->slug);
    fclose(fp);
    return validate_post(post, path);
}

