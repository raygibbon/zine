static int load_body_from_file(const char *path, long body_offset, char *body, int body_size) {
    FILE *fp;

    body[0] = '\0';

    fp = open_existing_file(path, "r");
    if (fp == NULL) {
        char message[256];
        sprintf(message, "Failed to open %s", path);
        set_content_error(message);
        return 0;
    }

    if (body_offset >= 0 && fseek(fp, body_offset, SEEK_SET) != 0) {
        char message[256];
        sprintf(message, "Failed to seek body in %s", path);
        set_content_error(message);
        fclose(fp);
        return 0;
    }

    while (fgets(g_body_line, sizeof(g_body_line), fp) != NULL) {
        trim_line_endings(g_body_line);
        if (!append_field_line(body, body_size, g_body_line, "body", path)) {
            fclose(fp);
            return 0;
        }
    }

    fclose(fp);
    return 1;
}

int load_post_body(const Post *post, char *body, int body_size) {
    return load_body_from_file(post->source_path, post->body_offset, body, body_size);
}

int load_page_body(const Page *page, char *body, int body_size) {
    return load_body_from_file(page->source_path, page->body_offset, body, body_size);
}

