static int load_entry_list(const char *list_path, const char *folder, int is_post, void *items, int max_items) {
    FILE *fp;
    int seen_count;
    int published_count;

    fp = open_existing_file(list_path, "r");
    if (fp == NULL) {
        if (is_post) {
            set_content_error("Failed to open posts.lst");
            return -1;
        }
        return 0;
    }

    seen_count = 0;
    published_count = 0;

    while (fgets(g_list_line, sizeof(g_list_line), fp) != NULL) {
        char normalized_name[16];
        int i;

        trim_line_endings(g_list_line);
        trim_whitespace(g_list_line);

        if (g_list_line[0] == '\0' || g_list_line[0] == '#') {
            continue;
        }

        if (!is_valid_list_entry_name(g_list_line)) {
            char message[256];
            sprintf(message, "Invalid list entry '%s' in %s", g_list_line, list_path);
            set_content_error(message);
            fclose(fp);
            return -1;
        }

        copy_string(normalized_name, sizeof(normalized_name), g_list_line);
        lowercase_string(normalized_name);

        for (i = 0; i < seen_count; ++i) {
            if (strings_equal_ignore_case(g_list_seen_names[i], normalized_name)) {
                char message[256];
                sprintf(message, "Duplicate list entry '%s' in %s", g_list_line, list_path);
                set_content_error(message);
                fclose(fp);
                return -1;
            }
        }

        if (seen_count < (int)(sizeof(g_list_seen_names) / sizeof(g_list_seen_names[0]))) {
            copy_string(g_list_seen_names[seen_count], sizeof(g_list_seen_names[seen_count]), normalized_name);
            ++seen_count;
        }

        join_path(g_list_path, sizeof(g_list_path), folder, normalized_name);

        if (is_post) {
            if (!load_post_file(g_list_path, normalized_name, &g_post_scratch)) {
                fclose(fp);
                return -1;
            }
            if (!g_post_scratch.draft) {
                if (published_count >= max_items) {
                    char message[256];
                    sprintf(message, "Too many posts; maximum is %d", max_items);
                    set_content_error(message);
                    fclose(fp);
                    return -1;
                }
                sprintf(g_post_scratch.output_name, "P%04d.HTM", published_count + 1);
                ((Post *)items)[published_count++] = g_post_scratch;
            }
        } else {
            if (!load_page_file(g_list_path, normalized_name, &g_page_scratch)) {
                fclose(fp);
                return -1;
            }
            if (!g_page_scratch.draft) {
                if (published_count >= max_items) {
                    char message[256];
                    sprintf(message, "Too many pages; maximum is %d", max_items);
                    set_content_error(message);
                    fclose(fp);
                    return -1;
                }
                ((Page *)items)[published_count++] = g_page_scratch;
            }
        }
    }

    fclose(fp);
    return published_count;
}

int load_posts(const char *list_path, Post *posts, int max_posts) {
    int count;

    count = load_entry_list(list_path, "posts", 1, posts, max_posts);
    if (count < 0) {
        return -1;
    }

    if (!validate_unique_post_slugs(posts, count)) {
        return -1;
    }

    return count;
}

int load_pages(const char *list_path, Page *pages, int max_pages) {
    int count;

    count = load_entry_list(list_path, "pages", 0, pages, max_pages);
    if (count < 0) {
        return -1;
    }

    if (!validate_unique_page_slugs(pages, count)) {
        return -1;
    }

    return count;
}

