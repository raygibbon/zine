static int validate_unique_post_slugs(const Post *posts, int count) {
    int i;
    int j;
    char message[256];

    for (i = 0; i < count; ++i) {
        for (j = i + 1; j < count; ++j) {
            if (strings_equal_ignore_case(posts[i].slug, posts[j].slug)) {
                sprintf(message, "Duplicate post slug '%s'", posts[i].slug);
                set_content_error(message);
                return 0;
            }
        }
    }

    return 1;
}

