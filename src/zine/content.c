static void set_content_error(const char *message) {
    copy_string(g_content_error, sizeof(g_content_error), message);
}

static int copy_field_value(char *dest, int dest_size, const char *value, const char *field_name, const char *path) {
    if ((int)strlen(value) >= dest_size) {
        sprintf(g_content_message, "Value too long for %s in %s", field_name, path);
        set_content_error(g_content_message);
        return 0;
    }

    copy_string(dest, dest_size, value);
    return 1;
}

static int append_field_line(char *dest, int dest_size, const char *line, const char *field_name, const char *path) {
    if (dest[0] != '\0' && !append_string_checked(dest, dest_size, "\n")) {
        sprintf(g_content_message, "%s too long in %s", field_name, path);
        set_content_error(g_content_message);
        return 0;
    }

    if (!append_string_checked(dest, dest_size, line)) {
        sprintf(g_content_message, "%s too long in %s", field_name, path);
        set_content_error(g_content_message);
        return 0;
    }

    return 1;
}

static int is_site_config_key(const char *key) {
    return strings_equal(key, "title") ||
        strings_equal(key, "banner") ||
        strings_equal(key, "theme") ||
        strings_equal(key, "tagline") ||
        strings_equal(key, "description") ||
        strings_equal(key, "about");
}

static int validate_post(const Post *post, const char *path) {
    if (post->title[0] == '\0') {
        sprintf(g_content_message, "Missing title in %s", path);
        set_content_error(g_content_message);
        return 0;
    }

    if (post->slug[0] == '\0') {
        sprintf(g_content_message, "Missing slug in %s", path);
        set_content_error(g_content_message);
        return 0;
    }

    if (!is_valid_dos_slug(post->slug)) {
        sprintf(g_content_message, "Invalid DOS slug '%s' in %s", post->slug, path);
        set_content_error(g_content_message);
        return 0;
    }

    if (!strings_equal_ignore_case(post->source_name, "")) {
        char expected[16];
        make_source_filename(expected, sizeof(expected), post->slug);
        if (!strings_equal_ignore_case(post->source_name, expected)) {
            sprintf(g_content_message, "Slug '%s' must match source file %s in %s", post->slug, expected, path);
            set_content_error(g_content_message);
            return 0;
        }
    }

    if (post->date[0] == '\0') {
        sprintf(g_content_message, "Missing date in %s", path);
        set_content_error(g_content_message);
        return 0;
    }

    if (!post->has_body || post->body_offset < 0) {
        sprintf(g_content_message, "Missing body in %s", path);
        set_content_error(g_content_message);
        return 0;
    }

    return 1;
}

